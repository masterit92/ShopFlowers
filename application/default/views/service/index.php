            <span><h1 class="BIGtext">Our Business Policies</h1></span>
            <span class="BIGtext">Substitution</span><br />
            <p>By placing an order, you are authorizing Flowershop.com to make substitutions 
                allowed by stringent guidelines. Flowershop.com will always attempt to fill the 
                order as closely as possible; however if the container, flowers, or other items 
                that comprise the order are unavailable then the order will be substituted with 
                content equal to the original value of the order.</p>
            <p>If an order is filled and it is determined that inappropriate or unauthorized 
                substitution has occurred, the filling florist will replace the arrangement at 
                no additional cost. However, we reserve the right to substitute alternate color 
                on roses; that is, should you place an order for a particular rose color and it 
                becomes unavailable, we will substitute an alternate rose color without 
                notification. The placement of your rose order implicitly authorizes us to 
                change rose color as necessary, without contacting you.</p>
            <p>Flowershop.com will gladly refund any purchases if the filling florist fails to 
                provide a resolution to any problem that occurs during the preparation and/or 
                delivery of the item.</p>
            <p class="BIGtext"><h1>Delivery</h1></p>
            <p>If any customer reports that an order is not delivered, we request 24 to 48 hours to contact 
                the filling florist to determine the arrangement's whereabouts. We reserve the right to attempt 
                a redelivery if (a) the recipient is not home during the time of delivery; or (b) no neighbor is 
                available to accept the delivery on behalf of the recipient.</p>
            <p>If it is determined that the filling florist did not make the delivery attempt, we will gladly
                provide you with a refund. We always ask that the filling florist provide an apology 
                arrangement to the recipient if it is determined that the filling florist was at fault, regardless of 
                the circumstances.</p>
